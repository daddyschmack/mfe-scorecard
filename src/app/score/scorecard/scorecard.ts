  private calculateTeamScores(teams: GolfTeam[], rounds: GolfRound[], tees: TeeBox, bestBallCount: number, gameType: 'match' | 'stroke' = 'match'): GolfTeam[] {
    // 1. Calculate Best Ball Score for EACH team independently
    const calculatedTeams = teams.map( team => {
      // Sync Roster with latest scores
      // We do this because TeamManager creates *new* round instances (placeholders).
      // We need to swap those placeholders with the 'live' rounds from this.rounds()
      // so that we can see the actual scores and calculate the winner.
      const updatedRoster = team.roster.map( member => {
        const roundById = rounds.find(r => r.id === member.id);
        if(roundById) return roundById;

        // Fallback: Match by Name (since TeamManager generates new IDs)
        return rounds.find(r => r.golfer.playerInfo?.name === member.golfer.playerInfo?.name) || member;
      });

      const holeScores: TeamHoleScore[] = [];
      let totalNet = 0;
      let totalGross = 0;

      for (let i= 0; i < 18; i++ ) {
        const par = tees.holeinfo?.[i]?.par || 4;

        // Check if at least one player on the team has a score for this hole
        const teamHasStartedHole = updatedRoster.some(r => (Number(r.player_score[i]?.totalScore) || 0) > 0);

        // Get all net scores for this hole from the roster
        const holeResult = updatedRoster.map(r => {
          const stat = r.player_score[i];
          const gross = Number(stat?.totalScore) || 0;

          // if no score entered, return high number to ignore
          if (gross === 0) {
            if (teamHasStartedHole) {
              // Max Score: Net Double Bogey (Par + 2)
              return { net: par + 2, gross: par + 2 };
            }
            return {net: 999, gross: 999};
          }

          // Calculate net (gross - pops)
          const playerHcap = r.golfer?.playerInfo?.handicap || 0;
          const holeHcap = stat?.holeHandicap ?? 0;

          // Use utility function (returns boolean)
          const getsPop = checkHandicapHole(holeHcap, playerHcap);
          const pops = getsPop ? 1 : 0;

          return {net: gross - pops, gross: gross};
        });

        // sort by net score
        holeResult.sort((a, b) => a.net - b.net);

        // Take the best N balls
        const bestBalls = holeResult.slice(0, bestBallCount);

        // If the team hasn't started the hole, the score is 0 (unplayed)
        // If they have started, any missing scores were converted to Max Score above, so we can safely sum.
        const net = !teamHasStartedHole ? 0 : bestBalls.reduce((sum, r) => sum + r.net, 0);
        const gross = !teamHasStartedHole ? 0 : bestBalls.reduce((sum, r) => sum + r.gross, 0);

        totalNet += net;
        totalGross += gross;
        holeScores.push({holeNumber: i + 1, netScore: net, grossScore: gross, outcome: 'l'}); // Default to 'l' (loss) or undefined, updated below
      }

      return {
        ...team,
        roster: updatedRoster,
        score: {...team.score, netScore: totalNet, grossScore: totalGross, roundScore: holeScores}
      };
    });

    // 2. Match Play Comparison (if exactly 2 teams and mode is match)
    if( calculatedTeams.length === 2 && gameType === 'match') {
      const teamA = calculatedTeams[0];
      const teamB = calculatedTeams[1];

      for (let i = 0; i < 18; i++){
        const scoreA = teamA.score.roundScore[i].netScore;
        const scoreB = teamB.score.roundScore[i].netScore;

        // Ignore holes not played (assuming > 100 is invalid/placeholder)
        if( scoreA === 0 || scoreB === 0 || scoreA >= 100 || scoreB >= 100) continue;

        if(scoreA < scoreB) {
          teamA.score.roundScore[i].outcome = 'w';
          teamB.score.roundScore[i].outcome = 'l';
        } else if( scoreB < scoreA){
          teamB.score.roundScore[i].outcome = 'w';
          teamA.score.roundScore[i].outcome = 'l';
        }
        // Ties are ignored (outcome remains undefined)
      }
    }

    return calculatedTeams;
  }

