  selectTeeBox(tee: TeeBox) {
    this.currentTees.set(tee);

    // Update the rounds with the new tee box info
    this.rounds.update(currentRounds => {
        return currentRounds.map(round => ({
            ...round,
            teeBox: {
                color: tee.color,
                par: tee.par,
                slope: tee.slope,
                type: tee.type
            }
        }));
    });
  }

  onTeeChange(event: Event) {
    const select = event.target as HTMLSelectElement;
    const index = parseInt(select.value, 10);
    const tees = this.golfCourseData().course?.tees;
    if (tees && tees[index]) {
      this.selectTeeBox(tees[index]);
    }
  }

